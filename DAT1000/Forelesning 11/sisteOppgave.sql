DROP SCHEMA IF EXISTS Hotelldatabase;
CREATE SCHEMA Hotelldatabase;
USE Hotelldatabase;

CREATE TABLE Kunde
(Mobilnr CHAR(8),
Fornavn CHAR(20),
Etternavn CHAR(40),
CONSTRAINT KundePK PRIMARY KEY(Mobilnr)
);

CREATE TABLE Rom
(Romnr CHAR(5),
Romtype CHAR(8),
CONSTRAINT RomPK PRIMARY KEY(Romnr)
);

DROP TABLE IF EXISTS Bestilling;
CREATE TABLE Bestilling
(Romnr CHAR(5),
Fradato DATE,
Tildato DATE NOT NULL,
Mobilnr CHAR(8),
CONSTRAINT BestillingPK PRIMARY KEY(Romnr,Fradato),
CONSTRAINT BestillingRomFK FOREIGN KEY(Romnr) REFERENCES Rom(Romnr),
CONSTRAINT BestillingKundeFK FOREIGN KEY(Mobilnr) REFERENCES Kunde(Mobilnr)
);

